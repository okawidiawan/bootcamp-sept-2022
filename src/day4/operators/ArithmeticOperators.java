package day4.operators;

public class ArithmeticOperators {
    
    public int add (int a, int b){
        return a + b;
    }
    
    public int subs (int a, int b){
        return a - b;
    }
    
    public int multiply (int a, int b){
        return a * b;
    }
    
    public int division (int a, int b){
        return a / b;
    }
    
    public int modulo (int a, int b){
        return a % b;
    }
}
