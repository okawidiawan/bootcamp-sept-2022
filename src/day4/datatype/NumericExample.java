package day4.datatype;

public class NumericExample {
    public static void main(String[] args) {
        boolean flag = true;
        System.out.println(flag);
        
        byte byteRange = 125;
        System.out.println(byteRange  + "cc");
        
        short shortRange = Short.MAX_VALUE;
        System.out.println(shortRange);
        
        int intRange = Integer.MIN_VALUE;
        System.out.println(intRange);
        
        double doubleRange = 10_000_000_000_000L;
        System.out.println(doubleRange);
        
        
    }
    
}
